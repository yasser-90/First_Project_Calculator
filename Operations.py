import math
class opeartion:
    def subtract(x, y):
        return x - y

    # This function adds two numbers
    def add(x, y):
        return x + y

    # This function multiplies two numbers
    def multiply(x, y):
        return x * y

    # This function divides two numbers
    def divide(x, y):
        return x / y
    
    def sin(x):
        return math.sin(x)
    
    def cos(x):
        return math.cos(x)
    
    def tan(x):
        return math.tan(x)
    
    def sqrt(x):
        return math.sqrt(x)
    
    def exp(x):
        return math.exp(x)
    
    
    def revers(x):
        return(str(x)[::-1])
    
    def divmod(x, y):
        result=divmod(x,y)
        return result
    
    def max(x,y):
        t=max(x,y)
        return t
    
    def min(x,y):
        t=min(x,y)
        return t